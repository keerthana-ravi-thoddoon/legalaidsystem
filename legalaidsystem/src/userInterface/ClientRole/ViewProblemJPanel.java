    private void populateDateComboBox() {
        dateJComboBox.removeAllItems();
        Date today = new Date();
        Calendar cal = Calendar.getInstance();
        for(DailySchedule ds : problem.getReceiver().getEmployee().getSchedule().getDailyScheduleList()) {
            if(ds.getDate().after(today)) {
                cal.setTime(ds.getDate());
                if(cal.get(Calendar.DAY_OF_WEEK) == Calendar.SATURDAY || cal.get(Calendar.DAY_OF_WEEK) == Calendar.SUNDAY){
                    continue;
                }
                boolean available = false;
                for(TimeSlot ts : ds.getTimeSlotList()) {
                    if(!ts.isOccupied()) {
                        available = true;
                        break;
                    }
                }
                if(available) {
                    dateJComboBox.addItem(ds);
                }
            }
        }
    }
    
    private void populateTimeComboBox(DailySchedule ds) {
        timeJComboBox.removeAllItems();
        for(TimeSlot ts : ds.getTimeSlotList()) {
            if(!ts.isOccupied()) {
                timeJComboBox.addItem(ts);
            }
        }
    }
    
    private void displayHelpful() {
        helpfulJLabel.setText("You think this answer is helpful.");
        helpfulJButton.setVisible(false);
    }

