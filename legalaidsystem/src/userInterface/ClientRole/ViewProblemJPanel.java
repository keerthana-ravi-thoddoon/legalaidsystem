    private void reserveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveJButtonActionPerformed
        
        DailySchedule ds = (DailySchedule)dateJComboBox.getSelectedItem();
        TimeSlot ts = (TimeSlot)timeJComboBox.getSelectedItem();
        int number = ds.getTimeSlotList().indexOf(ts);
        
        for(DailySchedule cds : problem.getSender().getEmployee().getSchedule().getDailyScheduleList()) {
            if(cds.toString().equals(ds.toString())) {
                if(cds.getTimeSlotList().get(number).isOccupied()) {
                    JOptionPane.showMessageDialog(null, "The time slot is already occupied! Please select another one!");
                    return;
                }
                else {
                    cds.getTimeSlotList().get(number).setOccupied(true);
                    cds.getTimeSlotList().get(number).setTask("Meeting with lawyer " + problem.getReceiver().getEmployee().getName());
                    cds.getTimeSlotList().get(number).setReservation(true);
                    cds.getTimeSlotList().get(number).setProblem(problem);
                    ts.setOccupied(true);
                    ts.setTask("Meeting with client " + problem.getSender().getEmployee().getName());
                    ts.setReservation(true);
                    ts.setProblem(problem);
                    problem.setStatus("Meeting Reserved");
                    JOptionPane.showMessageDialog(null, "Your reservation was successfully made!");
                    return;
                }
            }
        }
        DailySchedule nds = problem.getSender().getEmployee().getSchedule().createDailySchedule(ds.getDate());
        nds.getTimeSlotList().get(number).setOccupied(true);
        nds.getTimeSlotList().get(number).setTask("Meeting with lawyer " + problem.getReceiver().getEmployee().getName());
        nds.getTimeSlotList().get(number).setReservation(true);
        nds.getTimeSlotList().get(number).setProblem(problem);
        ts.setOccupied(true);
        ts.setTask("Meeting with client " + problem.getSender().getEmployee().getName());
        ts.setReservation(true);
        ts.setProblem(problem);
        problem.setStatus("Meeting Reserved");
        JOptionPane.showMessageDialog(null, "Your reservation was successfully made!");
        
        dateJComboBox.setEnabled(false);
        timeJComboBox.setEnabled(false);
        reserveJButton.setEnabled(false);
    }//GEN-LAST:event_reserveJButtonActionPerformed

