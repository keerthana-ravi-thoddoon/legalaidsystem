    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) requestJTable.getModel();
        
        model.setRowCount(0);
        for (ScheduleRequest request : account.getScheduleRequestQueue().getScheduleRequestList()){
            Object[] row = new Object[3];
            row[0] = request.getMessage();
            row[1] = request.getStatus();
            String reply = ((ResolvedScheduleRequest)request).getFeedback();
            row[2] = reply == null ? "Waiting" : reply;
            
            model.addRow(row);
        }
    }

