    private void sendRequestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendRequestJButtonActionPerformed

        String message = messageJTextField.getText();
        if(message.equals("") || message.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter something to send.");
            return;
        }

        ResolvedScheduleRequest request = new ResolvedScheduleRequest();
        request.setMessage(message);
        request.setSender(account);
        request.setReceiver(account.getEmployee().getAssistant());
        request.setStatus("Sent");
        request.setFeedback("");

        account.getScheduleRequestQueue().getScheduleRequestList().add(request);
        account.getEmployee().getAssistant().getScheduleRequestQueue().getScheduleRequestList().add(request);

        JOptionPane.showMessageDialog(null, "Your request was successfully sent!");
        
        populateRequestTable();
    }//GEN-LAST:event_sendRequestJButtonActionPerformed

